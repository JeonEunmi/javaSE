package com.test;

public class Sample019 {

	public static void main(String[] args) {
		
		// 연산자
		// 대입(할당) 연산자
		// =, +=, -=, *=, /=, %=
		// -> 값을 변수에 (연산 수행 후 : '='은 제외) 저장하는 행위
		
		int a = 10;
		
		a += 10;	// 현재 변수 a에 10을 더한 값을 저장하여라. 20
		System.out.println(a);
		
		a = a + 10;	// 30 , 위 연산식과 동일한 식
		System.out.println(a);
	}

}
 