package com.test;

public class Sample072 {

	public static void main(String[] args) {
		
		// String 클래스 (참조 자료형)
		// valueOf() 메소드
		// -> static 메소드
		// -> 주어진 기본형 자료를 String 자료형으로 변환
		
		int a = 10;
		char b = 'A';
		double c = 12.345;
		
		System.out.println(String.valueOf(a));   //'10'
		System.out.println(String.valueOf(b));
		System.out.println(String.valueOf(c));

	}

}
